# Запрашиваем количество билетов
num_tickets = int(input("Введите количество билетов, которые хотите приобрести: "))

# Инициализируем переменную для хранения общей стоимости
total_cost = 0

# Запрашиваем возраст каждого посетителя и рассчитываем стоимость билета
for i in range(num_tickets):
    age = int(input(f"Введите возраст посетителя для билета {i + 1}: "))
    if age < 18:
        ticket_cost = 0
    elif 18 <= age <= 25:
        ticket_cost = 990
    else:
        ticket_cost = 1390
    total_cost += ticket_cost

# Применяем скидку, если покупается более 3 билетов
if num_tickets > 3:
    total_cost *= 0.9  # Скидка 10%

# Выводим сумму к оплате
print(f"Сумма к оплате: {total_cost} руб.")